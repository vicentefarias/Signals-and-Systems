Range of acceptable K values and explanation for 2 K values:
25 <= K <= 64

For this range of values of K, T(s) has its poles within
the shaded region in the complex plane. When K = 16, the 
system is critically damped and both poles lie at -4+0j. If K>16
the poles become complex and move vertically along the real = -4
component. The poles enter the shaded region when K = 25 and leave
when K = 64. The unit step function of each value verifies the given
specifications.
